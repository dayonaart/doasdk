  return TextStyle(
      fontWeight: FontWeight.w600, fontSize: fontSize, color: fontColor);
}

TextStyle textStyleW700({double? fontSize, Color? fontColor}) {
  return TextStyle(
      fontWeight: FontWeight.w700, fontSize: fontSize, color: fontColor);
}
